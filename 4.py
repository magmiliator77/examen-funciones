#4. Años bisiestos (2,5 puntos): Crear un programa que genere 100 números aleatorios entre 1 y 2024 (serán como años de nuestra era) y los guarde en una lista. Debemos crear una función que se llame esBisiesto a la que le pasemos un año y nos diga si ese años es bisiesto o no (True o False). Aplicar esa función para contar cuántos años bisiestos hay en nuestra lista y mostrar un mensaje por pantalla indicándolo.



import random

# Función para determinar si un año es bisiesto
def es_bisiesto(anio):
    
    return (anio % 4 == 0 and anio % 100 != 0) or (anio % 400 == 0)

# Generamos la lista de años aleatorios
anios = []

contador_bisiestos = 0

contador = 0

while contador < 100:

    anio = random.randint(1, 2024)

    anios.append(anio)


    if es_bisiesto(anio):

        contador_bisiestos += 1

    contador += 1

# Mostramos el resultado
print(f"En la lista hay {contador_bisiestos} años bisiestos.")



